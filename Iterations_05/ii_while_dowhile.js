// while Loop 
let index = 0
while (index <= 10) {
    console.log(`Value of index is ${index}`);
    index = index + 2
}

let myArray = ['Flash' , 'Batman', 'Superman']
let arr = 0
while(arr < myArray.length){
    console.log(`My ${arr} Superhero Name is ${myArray[arr]}`);
    arr ++
}

// do_while Loop

let score = 1
do {
    console.log(`Value is ${score}`);
    score++
} while (score <= 10);

let scores = 11
do {
    console.log(`Value is ${scores}`);
    scores++
} while (scores <= 10);